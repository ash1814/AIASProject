    public void generatePieChart() throws SQLException 
    {
            DefaultPieDataset dataSet = new DefaultPieDataset();
                
            con = DriverManager.getConnection( "jdbc:mysql://localhost:3306/aias", "root", "");
            pst = con.prepareStatement("Select noofstudent,schoolcode from admission group by schoolcode");
            rs = pst.executeQuery();
            
            while(rs.next())
            {
                dataSet.setValue(rs.getString("schoolcode"),Integer.parseInt(rs.getString("noofstudent")));

            }

		JFreeChart chart = ChartFactory.createPieChart3D(
				"School wise interest data 2019", dataSet, true, false, true);
                
                chart.setBackgroundPaint(Color.white);
                panel1.setLayout(new java.awt.BorderLayout());
                panel1.setBackground(Color.WHITE);
 
                ChartPanel CP = new ChartPanel(chart);
                
                panel1.add(CP,BorderLayout.CENTER);
                panel1.validate();
		
    }

